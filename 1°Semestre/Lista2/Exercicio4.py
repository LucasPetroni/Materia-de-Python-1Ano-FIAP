numero1 = int(input("Dgite o primeiro numero: "))
numero2 = int(input("Dgite o segundo numero: "))

print(numero1, "+", numero2, "=", numero1+numero2)
print(numero1, "*", numero2, "=", numero1*numero2)
print(numero1, "/", numero2, "=", numero1/numero2)
print(numero1, "%", numero2, "=", numero1%numero2)

